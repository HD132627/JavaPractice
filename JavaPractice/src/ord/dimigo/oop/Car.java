package ord.dimigo.oop;

public class Car {
	
	private String Company;
	private String Model;
	private String Color;
	private int MaxSpeed;
	private int Price;

	public String getCompany() {
		return Company;
	}
	public String getModel() {
		return Model;
	}
	public String getColor() {
		return Color;
	}
	
	public int getMaxSpeed() {
		return MaxSpeed;
	}
	public int getPrice() {
		return Price;
	}
	
}